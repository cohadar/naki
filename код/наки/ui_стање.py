class UIСтање():
    def __init__(бре):
        бре.претходни_текст = None
        бре.зелено = False

    def наслов(бре, текст):
        промена_боје_наслова = False
        if бре.претходни_текст is None:
            бре.претходни_текст = текст
        if бре.претходни_текст != текст:
            бре.зелено = not бре.зелено
            промена_боје_наслова = True
        бре.претходни_текст = текст
        return промена_боје_наслова

